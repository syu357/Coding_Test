def solution(letter):
    answer = ''
    
    morse = {
    '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
    '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
    '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
    '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
    '-.--':'y','--..':'z'}
    
    # letter를 공백을 기준으로 쪼개서 딕셔너리 매칭
    alpha = letter.split()
    
    for i in alpha:
        answer += morse[i] 
    
    return answer